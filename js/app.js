// Chamada api
let productPromise = fetch(
  "http://makeup-api.herokuapp.com/api/v1/products.json"
);
productPromise.then((resp) => {
  resp.json().then((product) => {
    let productInfo = productItem(product);
    document.getElementById("app").innerHTML = productInfo;
  });
});

